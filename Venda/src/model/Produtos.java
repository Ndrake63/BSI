package model;

/**
 *
 * @author alunos
 */
public class Produtos {
    float refri;
    float pizza;
    float salgado;
    float pastel;
    float torrada;
    float doce;
    float cafe;

    public Produtos(float refri, float pizza, float salgado, float pastel, float torrada, float doce, float cafe) {
        this.refri = refri;
        this.pizza = pizza;
        this.salgado = salgado;
        this.pastel = pastel;
        this.torrada = torrada;
        this.doce = doce;
        this.cafe = cafe;
    }

    public float getRefri() {
        return refri;
    }

    public void setRefri(float refri) {
        this.refri = refri;
    }

    public float getPizza() {
        return pizza;
    }

    public void setPizza(float pizza) {
        this.pizza = pizza;
    }

    public float getSalgado() {
        return salgado;
    }

    public void setSalgado(float salgado) {
        this.salgado = salgado;
    }

    public float getPastel() {
        return pastel;
    }

    public void setPastel(float pastel) {
        this.pastel = pastel;
    }

    public float getTorrada() {
        return torrada;
    }

    public void setTorrada(float torrada) {
        this.torrada = torrada;
    }

    public float getDoce() {
        return doce;
    }

    public void setDoce(float doce) {
        this.doce = doce;
    }

    public float getCafe() {
        return cafe;
    }

    public void setCafe(float cafe) {
        this.cafe = cafe;
    }
    
    
    
            
}
